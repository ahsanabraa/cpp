#include<iostream>
using namespace std;
int main() {
    //Mencetak bilangan genap
    cout<<"Mencetak bilangan genap"<<endl;
    int i;
    for(i=10; i>=1; i-=2)
    {
    cout<<i<<" ";
    }
    cout<<"\n\n";

    //Mencetak bilangan ganjil
    cout<<"Mencetak bilangan ganjil"<<endl;
    int j;
    for(j=9; j>=1; j-=2)
    {
    cout<<j<<" ";
    }
    cout<<"\n\n";

    //Mencetak bilangan 10-1
    cout<<"Mencetak bilangan 10-1"<<endl;
    int k;
    for(k=10; k>=1; k--)
    {
    cout<<k<<" ";
    }
    cout<<"\n\n";

    return 0;
}